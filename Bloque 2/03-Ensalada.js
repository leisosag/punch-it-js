const n1 = parseInt(prompt("Ingresa el primer número"));
const n2 = parseInt(prompt("Ingresa el segundo número"));
const n3 = parseInt(prompt("Ingresa el tercer número"));
const n4 = parseInt(prompt("Ingresa el cuarto número"));
const n5 = parseInt(prompt("Ingresa el quinto número"));
const n6 = parseInt(prompt("Ingresa el sexto número"));

const suma = n1 + n2 + n3;
const producto = n3 * n4 * n5 + n6;
console.log(`La suma de ${n1} + ${n2} + ${n3} es ${suma}`);
console.log(`El producto de ( ${n3} * ${n4} * ${n5} ) + ${n6} es ${producto}`);
